#include "libft.h"

void	ft_putnbr(long long n)
{
	size_t	nb;

	nb = n;
	if (n < 0)
	{
		ft_putchar('-');
		nb = -n;
	}
	if (nb > 9)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	if (nb <= 9)
		ft_putchar(nb + '0');
}
